"""Промпты для двухэтапного пайплайна разбора тендера.

Этап 1 (extraction) — по ОДНОМУ документу за раз: извлечь факты + источник/страницу.
Этап 2 (synthesis)  — объединить извлечённые факты из всех документов в финальный анализ.

Разделение на два этапа решает две задачи разом:
  - не упираемся в маленькое контекстное окно российских LLM (кормим по одному документу)
  - каждый факт снабжён источником — требование Explainability из Описание.txt
"""
import json


def build_extraction_prompt(doc_name: str, doc_text: str) -> str:
    return f"""Ты — эксперт по госзакупкам РФ. Перед тобой ОДИН документ тендерной документации.

Название файла: {doc_name}

Текст документа:
---
{doc_text}
---

Извлеки ВСЕ факты, которые относятся к следующим категориям, ЕСЛИ они есть в этом документе.
Для каждого факта укажи: что нашёл, дословную цитату или номер пункта (source), и страницу если видно.
Если категории в этом документе нет — не включай её в ответ.

Категории:
- lawType: тип закупки (44-ФЗ / 223-ФЗ / коммерческая / корпоративная) — по каким законам/положениям ссылается документ
- procurementMethod: СПОСОБ закупки — конкурс / аукцион / запрос котировок / запрос предложений / у единственного поставщика / электронный магазин и т.п. Ищи явное название способа, обычно в Информационной карте или извещении ("Способ определения поставщика", "Форма проведения закупки")
- subject: предмет закупки — что именно закупается (товар/работа/услуга), кратко
- customer: наименование заказчика
- deliveryPlace: место поставки/выполнения работ/оказания услуг
- deliveryTerm: срок и порядок поставки — ОБЯЗАТЕЛЬНО укажи, разовая это поставка одной партией, или поставка по заявкам/партиями в течение периода
- paymentTerms: условия и срок оплаты (например "в течение 7 рабочих дней после поставки")
- nmck: начальная (максимальная) цена контракта/договора
- deadline: дата и время окончания подачи заявок
- bidSec: обеспечение заявки (сумма, процент, условия)
- contractSec: обеспечение исполнения контракта/договора
- retender: упоминание переторжки / дополнительного запроса цены
- evaluationCriteria: критерии оценки заявок. ВАЖНО: укажи явно, единственный критерий — цена, или есть ДОПОЛНИТЕЛЬНЫЕ неценовые критерии (опыт участника — например, N успешно исполненных контрактов; квалификация/кадровые ресурсы; деловая репутация; наличие оборудования и т.п.). Если критериев несколько — перечисли каждый отдельным фактом с указанием его значимости (вес в баллах или %, если есть)

- requiredDocs: перечень документов и сведений для подачи заявки. КРИТИЧЕСКИ ВАЖНО:
  извещения обычно дают список пунктом (например "заявка должна содержать: 1)...2)...3)...")
  — НЕ пиши в value "см. пункт 15" или "перечень указан в п. 15 извещения". Это бесполезно
  пользователю. Вместо этого РАЗВЕРНИ каждый под-пункт СПИСКА в отдельный факт с понятной
  формулировкой на русском языке: что именно за документ/сведение, а не номер ссылки.
  Пример правильного разбора пункта "1) наименование, адрес юр.лица, учредительный документ":
  → отдельный факт value="Наименование, адрес участника и учредительный документ (устав)".
  Если в списке объединены несколько под-требований одной темы (например под-пункты а,б,в,г,д
  одной "декларации, подтверждающей..." — это про несостоятельность, неприостановку деятельности,
  отсутствие недоимки по налогам, отсутствие судимости, отсутствие привлечения к ответственности
  по ст. 19.28 КоАП) — можно объединить их в ОДИН факт "Декларация о соответствии участника
  требованиям (несостоятельность, налоговая задолженность, судимости и т.п.)", но никогда не
  просто номер пункта без содержания.

- risks: ВСЕ важные условия, которые могут повлиять на решение участвовать или на подготовку заявки.
  Активно ищи и не пропускай, если есть в документе:
  • Ограничение участия только для субъектов МСП — обязательно как факт (не полагайся что это
    проверят автоматически)
  • Требование к формату файла самой заявки (например "заявка в формате *.doc, заверенная ЭЦП")
  • Правило "подать можно только ОДНУ заявку, изменения не допускаются"
  • Последствия уклонения от заключения договора (штрафы, реестр недобросовестных поставщиков,
    понуждение через суд)
  • Сроки на разъяснения/уточнения по документации, если короткие
  • Специфические требования к товару/услуге (бренд без аналогов, сроки поставки, штрафы)
  • Национальный режим / запрет на иностранное происхождение товара
  • Любые другие условия, которые обычный участник тендера легко может упустить при беглом чтении
  Если после внимательного поиска действительно ничего из этого нет — не выдумывай, оставь пустым.

Верни СТРОГО валидный JSON без markdown-блоков, массив фактов:
{{
  "facts": [
    {{"category": "procurementMethod", "value": "Открытый конкурс в электронной форме", "source": "Информационная карта, п. 2", "page": null}},
    {{"category": "requiredDocs", "value": "Наименование, адрес участника и учредительный документ (устав)", "source": "п. 15(1) извещения", "page": null}},
    {{"category": "risks", "value": "Участвовать могут только субъекты МСП", "source": "Заголовок извещения", "page": null}}
  ]
}}"""


def build_synthesis_prompt(profile: dict, today: str, all_facts: list) -> str:
    return f"""Ты — эксперт по госзакупкам РФ (44-ФЗ, 223-ФЗ, коммерческие и корпоративные закупки).
Ниже — факты, извлечённые из всех документов тендера (каждый факт со ссылкой на источник).
Собери из них финальный анализ для компании-участника.

ГЛАВНАЯ ЦЕЛЬ: пользователь должен, прочитав твой анализ, принять решение — стоит ли участвовать
в этой закупке, и если да — быстро понять, что нужно подготовить. Твоя задача — избавить его от
необходимости самому читать длинные юридические документы и искать ссылки на пункты. Каждое поле
должно содержать РЕШЁННЫЙ, развёрнутый ответ, а не отсылку "см. документацию" или номер пункта.

Ты НЕ занимаешься ценовой стратегией и не советуешь, по какой цене торговаться — это решает сам
участник исходя из своей себестоимости. Твоя задача — понимание требований, оценка рисков, помощь
с составом заявки.

Текущая дата: {today or "не передана"}.
Профиль участника:
{json.dumps(profile, ensure_ascii=False, indent=2)}

Извлечённые факты из документов:
{json.dumps(all_facts, ensure_ascii=False, indent=2)}

━━━ ПРАВИЛА ПО ТИПУ ЗАКУПКИ (определи lawType по фактам выше) ━━━

[44-ФЗ]:
• bidSec: 0,5–1% от НМЦК (ст. 44), если в фактах нет точной суммы
• contractSec: 5% от НМЦК (ст. 96), если в фактах нет точной суммы
• retender: "не предусмотрена", если в фактах не указано иное

[223-ФЗ / коммерческая / корпоративная]:
• bidSec / contractSec: ТОЛЬКО если есть в фактах, иначе "не установлено"
• retender: бери из фактов, иначе "не указано в документации"

━━━ ПОЛЕ type ━━━
Собери его из lawType + procurementMethod, например: "223-ФЗ · Открытый конкурс в электронной форме".
Если procurementMethod не найден ни в одном факте — "не указано", НЕ придумывай.

━━━ ПОЛЕ evaluationCriteria ━━━
Собери массив из фактов категории evaluationCriteria. Если фактов такой категории нет вообще —
верни пустой массив [] (это означает: в документах не найдено явного описания критериев,
не то что критерий один — цена, эти два состояния РАЗНЫЕ, не путай их).
Если найден только один факт про критерий "цена" — так и верни один элемент.

━━━ ПОЛЕ requiredDocs ━━━
Собери из фактов категории requiredDocs. Каждый факт → {{"name": разверни в понятную формулировку
(НЕ номер пункта!), "required": true (почти всегда true для этой категории), "note": источник если
полезно}}. Если факт всё же выглядит как голая ссылка на пункт без содержания — постарайся вывести
содержание из общего контекста остальных фактов, а не оставляй как есть.

━━━ ПОЛЕ risks ━━━
Собери из фактов категории risks: {{"type":"warn" если это ограничение/риск для участника,
"info" если это просто важная информация без угрозы, "t":"короткий заголовок", "d":"пояснение"}}.
Не оставляй массив пустым, если в фактах есть хотя бы один risks-факт.

━━━ ОБЩИЕ ПРАВИЛА ━━━
• Используй ТОЛЬКО факты из списка выше. Не придумывай цифры и формулировки.
• НДС 22% с 01.01.2026 — нормальная ставка (ФЗ № 425-ФЗ), не ошибка.
• Если данных нет — "не указано".
• deadline: "ДД.ММ.ГГГГ в ЧЧ:ММ · осталось N дн." Если истёк — "срок подачи истёк".

Верни СТРОГО валидный JSON без markdown-блоков:
{{
  "fits": true/false,
  "fitTitle": "вердикт 3-5 слов",
  "fitDesc": "1-2 предложения — прямо отвечает на вопрос \\"стоит ли участвовать\\"",
  "lawType": "44-ФЗ / 223-ФЗ / коммерческая / корпоративная",
  "subject": "предмет закупки",
  "customer": "наименование заказчика",
  "deliveryPlace": "место поставки/выполнения",
  "deliveryTerm": "срок и порядок поставки (разовая / по заявкам)",
  "paymentTerms": "условия оплаты",
  "nmck": "сумма",
  "nmckNote": "про НДС/период если есть",
  "type": "по правилам поля type выше",
  "deadline": "по формату выше",
  "bidSec": "сумма или не установлено",
  "bidSecNote": "основание",
  "contractSec": "сумма или не установлено",
  "contractSecNote": "основание",
  "retender": "предусмотрена / не предусмотрена / не указано в документации",
  "evaluationCriteria": ["критерий 1 с весом если есть", "критерий 2..."],
  "requiredDocs": [{{"name":"развёрнутая формулировка","required":true,"note":"источник"}}],
  "risks": [{{"type":"warn","t":"заголовок","d":"пояснение"}}]
}}"""
