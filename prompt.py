"""Промпт для разбора тендера. Вынесен отдельно, чтобы main.py остался компактным."""
import json


def build_prompt(profile: dict, today: str) -> str:
    return f"""Ты — эксперт по госзакупкам РФ (44-ФЗ, 223-ФЗ, коммерческие и корпоративные закупки).
Разбери документацию тендера для компании-участника.

Текущая дата: {today or "не передана"}.
Профиль участника:
{json.dumps(profile, ensure_ascii=False, indent=2)}

━━━ ШАГ 1: ОПРЕДЕЛИ ТИП ЗАКУПКИ ━━━
• 44-ФЗ — госзаказчик, контрактная система (аукцион, конкурс, запрос котировок)
• 223-ФЗ — корпоративный заказчик (АО, ГУП, МУП), ссылается на Положение о закупке
• коммерческая — без ссылок на 44-ФЗ / 223-ФЗ
• корпоративная — ссылка на положение корпорации (ЕПОЗ, Ростех и др.)

━━━ ШАГ 2: ПРАВИЛА ПО ТИПУ ━━━

[44-ФЗ]:
• bidSec: 0,5–1% от НМЦК (ст. 44). bidSecNote: "ст. 44 44-ФЗ, блокируется на спецсчёте"
• contractSec: 5% от НМЦК (ст. 96). contractSecNote: "ст. 96 44-ФЗ"
• corridor: "Старт аукциона — {{НМЦК}}. Порог антидемпинга — {{НМЦК × 0,75}} (−25%, ст. 37 44-ФЗ)."
• corridorNote: "Ниже порога обеспечение контракта вырастет (ст. 37 44-ФЗ). Минимальная цена — ваша себестоимость."
• retender: "не предусмотрена"

[223-ФЗ]:
• bidSec: ТОЛЬКО из документа. Если не указано — "не установлено"
• contractSec: ТОЛЬКО из документа. Если не указано — "не установлено"
• corridor: "Старт — {{НМЦК}}. Победитель — участник с наименьшей ценой."
• corridorNote: "Ст. 37 44-ФЗ (антидемпинг) к данной закупке не применяется. Нижний предел — ваша себестоимость."
• retender: ищи "переторжка" / "дополнительный запрос цены". Если явно "не предусмотрена" — так и пиши. Иначе — "не указано в документации"

[коммерческая / корпоративная]:
• Все суммы ТОЛЬКО из документа
• corridor: "Старт — {{НМЦК}}." без антидемпинга
• corridorNote: "Условия торга определяются документацией. Нижний предел — ваша себестоимость."
• retender: ищи в документе, иначе "не указано в документации"

━━━ ШАГ 3: ДОКУМЕНТЫ ЗАЯВКИ ━━━
Найди раздел "Перечень документов" / "Состав заявки" / "Требования к участнику".
Верни массив: {{"name":"...","required":true/false,"note":"..."}}
required: true — обязателен. false — факультативен. Если раздела нет — [].

━━━ ОБЩИЕ ПРАВИЛА ━━━
• Документ первичен. Цифры только из документа.
• НДС 22% с 01.01.2026 — нормальная ставка (ФЗ № 425-ФЗ), не ошибка.
• Если данных нет — "не указано".
• deadline: "ДД.ММ.ГГГГ в ЧЧ:ММ · осталось N дн." Если истёк — "срок подачи истёк".

Верни СТРОГО валидный JSON без markdown-блоков:
{{
  "fits": true/false,
  "fitTitle": "вердикт 3-5 слов",
  "fitDesc": "1-2 предложения",
  "lawType": "44-ФЗ / 223-ФЗ / коммерческая / корпоративная",
  "nmck": "сумма",
  "nmckNote": "про НДС/период если есть",
  "type": "тип + способ закупки",
  "deadline": "по формату выше",
  "bidSec": "сумма или не установлено",
  "bidSecNote": "основание",
  "contractSec": "сумма или не установлено",
  "contractSecNote": "основание",
  "corridor": "по правилам типа",
  "corridorNote": "оговорка",
  "retender": "предусмотрена / не предусмотрена / не указано в документации",
  "requiredDocs": [{{"name":"...","required":true,"note":"..."}}],
  "risks": [{{"type":"warn","t":"заголовок","d":"пояснение"}}]
}}"""
