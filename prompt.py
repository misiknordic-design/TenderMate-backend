"""Промпты для двухэтапного пайплайна разбора тендера.

Этап 1 (extraction) — по ОДНОМУ документу за раз: извлечь факты + источник/страницу.
Этап 2 (synthesis)  — объединить извлечённые факты из всех документов в финальный анализ.

Разделение на два этапа решает две задачи разом:
  - не упираемся в маленькое контекстное окно российских LLM (кормим по одному документу)
  - каждый факт снабжён источником — требование Explainability из Описание.txt
"""
import json


def build_extraction_prompt(doc_name: str, doc_text: str) -> str:
    return f"""Ты — эксперт по госзакупкам РФ. Перед тобой ОДИН документ тендерной документации.

Название файла: {doc_name}

Текст документа:
---
{doc_text}
---

Извлеки ВСЕ факты, которые относятся к следующим категориям, ЕСЛИ они есть в этом документе.
Для каждого факта укажи: что нашёл, дословную цитату или номер пункта (source), и страницу если видно.
Если категории в этом документе нет — не включай её в ответ.

Категории:
- lawType: тип закупки (44-ФЗ / 223-ФЗ / коммерческая / корпоративная) — по каким законам/положениям ссылается документ
- nmck: начальная (максимальная) цена контракта/договора
- deadline: дата и время окончания подачи заявок
- bidSec: обеспечение заявки (сумма, процент, условия)
- contractSec: обеспечение исполнения контракта/договора
- retender: упоминание переторжки / дополнительного запроса цены
- requiredDocs: перечень документов, которые нужно подать в составе заявки
- risks: любые важные условия — сроки поставки, штрафы, специфические требования к товару/услуге, требования к участнику, национальный режим и т.п.

Верни СТРОГО валидный JSON без markdown-блоков, массив фактов:
{{
  "facts": [
    {{"category": "nmck", "value": "268 972,30 ₽", "source": "Раздел №3, обоснование НМЦД", "page": null}},
    {{"category": "risks", "value": "Товар строго брендовый VILEDA — эквиваленты не допускаются", "source": "п. 7 извещения", "page": 2}}
  ]
}}"""


def build_synthesis_prompt(profile: dict, today: str, all_facts: list) -> str:
    return f"""Ты — эксперт по госзакупкам РФ (44-ФЗ, 223-ФЗ, коммерческие и корпоративные закупки).
Ниже — факты, извлечённые из всех документов тендера (каждый факт со ссылкой на источник).
Собери из них финальный анализ для компании-участника.

Текущая дата: {today or "не передана"}.
Профиль участника:
{json.dumps(profile, ensure_ascii=False, indent=2)}

Извлечённые факты из документов:
{json.dumps(all_facts, ensure_ascii=False, indent=2)}

━━━ ПРАВИЛА ПО ТИПУ ЗАКУПКИ (определи lawType по фактам выше) ━━━

[44-ФЗ]:
• bidSec: 0,5–1% от НМЦК (ст. 44), если в фактах нет точной суммы
• contractSec: 5% от НМЦК (ст. 96), если в фактах нет точной суммы
• corridor: "Старт аукциона — {{НМЦК}}. Порог антидемпинга — {{НМЦК × 0,75}} (−25%, ст. 37 44-ФЗ)."
• corridorNote: "Ниже порога обеспечение контракта вырастет (ст. 37 44-ФЗ). Минимальная цена — ваша себестоимость."
• retender: "не предусмотрена", если в фактах не указано иное

[223-ФЗ]:
• bidSec / contractSec: ТОЛЬКО если есть в фактах, иначе "не установлено"
• corridor: "Старт — {{НМЦК}}. Победитель — участник с наименьшей ценой."
• corridorNote: "Ст. 37 44-ФЗ (антидемпинг) к данной закупке не применяется. Нижний предел — ваша себестоимость."
• retender: бери из фактов, иначе "не указано в документации"

[коммерческая / корпоративная]:
• Все суммы ТОЛЬКО из фактов
• corridor: "Старт — {{НМЦК}}." без антидемпинга
• retender: бери из фактов, иначе "не указано в документации"

━━━ ОБЩИЕ ПРАВИЛА ━━━
• Используй ТОЛЬКО факты из списка выше. Не придумывай цифры.
• НДС 22% с 01.01.2026 — нормальная ставка (ФЗ № 425-ФЗ), не ошибка.
• Если данных нет — "не указано".
• deadline: "ДД.ММ.ГГГГ в ЧЧ:ММ · осталось N дн." Если истёк — "срок подачи истёк".
• requiredDocs собери из фактов категории requiredDocs: {{"name":"...","required":true/false,"note":"..."}}
• risks собери из фактов категории risks: {{"type":"warn","t":"заголовок","d":"пояснение"}}

Верни СТРОГО валидный JSON без markdown-блоков:
{{
  "fits": true/false,
  "fitTitle": "вердикт 3-5 слов",
  "fitDesc": "1-2 предложения",
  "lawType": "44-ФЗ / 223-ФЗ / коммерческая / корпоративная",
  "nmck": "сумма",
  "nmckNote": "про НДС/период если есть",
  "type": "тип + способ закупки",
  "deadline": "по формату выше",
  "bidSec": "сумма или не установлено",
  "bidSecNote": "основание",
  "contractSec": "сумма или не установлено",
  "contractSecNote": "основание",
  "corridor": "по правилам типа",
  "corridorNote": "оговорка",
  "retender": "предусмотрена / не предусмотрена / не указано в документации",
  "requiredDocs": [{{"name":"...","required":true,"note":"..."}}],
  "risks": [{{"type":"warn","t":"заголовок","d":"пояснение"}}]
}}"""
